public class Animal3 {
	public int age;
	private String name;
	public Animal3(int age, String name){
		super();
		this.age = age;
		this.name = name;
		System.out.println("First Constructor");
	}
	public Animal3(int age){
		super();
		this.age = age;
		this.name = name;
		System.out.println("Second Constructor");
	}
}
